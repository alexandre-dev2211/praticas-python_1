# Recebendo valores do usuário e realizando a soma de seus quadrados:

v1 = int(input("Informe o Valor 1: "))

v2 = int(input("Informe o valor 2: "))

v3 = int(input("Informe o valor 3: "))

print("Os valores informados são: ",v1, v2, v3)

q1 = v1 * v1
q2 = v2 * v2
q3 = v3 * v3

print("Os rescpectivos quadrados são: ", q1, q2, q3)

soma_quadrado = q1 + q2 + q3

print("A soma dos quadrados é: ", soma_quadrado)
