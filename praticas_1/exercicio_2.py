# Recebendo um Inteiro e Exibindo a Soma do Sucessor de Seu Triplo com o Antecessor de Seu Dobro:

inteiro = int(input("Digite um número inteiro: "))

form1 = (inteiro * 2) - 1

form2 = (inteiro * 3) + 1

resultado = form1 + form2

print("O Antecessor do Dobro do Número informado é: ", form1)

print("Sucessor do Triplo do Número Informado é: ", form2)

print("O Resultado da Soma é: ", resultado)
